//! Direct `ort-sys::RunAsync` verification harness for the CUDA path.
//!
//! Run with: `cargo run --bin ort-verify --features cuda -- --model model.onnx`

use std::ffi::{CStr, CString, c_char, c_void};
use std::ptr::NonNull;
use std::sync::{
    Arc, Mutex,
    atomic::{AtomicU8, Ordering},
};

use clap::Parser;
use ort::{
    AsPointer, api,
    execution_providers::CUDAExecutionProvider,
    memory::{AllocationDevice, AllocatorType, MemoryInfo, MemoryType},
    session::Session,
    sys,
};

use disrust::constants::FEATURE_DIM as FDIM;

const BATCH_PENDING: u8 = 0;
const BATCH_READY: u8 = 1;
const BATCH_FAILED: u8 = 2;

#[derive(Parser)]
struct Args {
    /// Path to the ONNX model file.
    #[arg(short, long, default_value = "model.onnx")]
    model: String,

    /// Number of vectors to submit in the verification batch.
    #[arg(short = 'n', long, default_value_t = 4)]
    batch: usize,

    /// Validate exact outputs for the model generated by `scripts/generate_verify_model.py`.
    #[arg(long)]
    validate_generated_model: bool,
}

fn status_message(status: sys::OrtStatusPtr) -> String {
    if status.0.is_null() {
        return "unknown ORT error".to_string();
    }
    let message = unsafe { (api().GetErrorMessage)(status.0) };
    let message = unsafe { CStr::from_ptr(message) }
        .to_string_lossy()
        .into_owned();
    unsafe { (api().ReleaseStatus)(status.0) };
    message
}

fn check_status(status: sys::OrtStatusPtr, context: &str) {
    if !status.0.is_null() {
        eprintln!("{context}: {}", status_message(status));
        std::process::exit(1);
    }
}

struct OrtValueHandle(NonNull<sys::OrtValue>);

impl OrtValueHandle {
    fn as_ptr(&self) -> *mut sys::OrtValue {
        self.0.as_ptr()
    }
}

impl Drop for OrtValueHandle {
    fn drop(&mut self) {
        unsafe { (api().ReleaseValue)(self.0.as_ptr()) };
    }
}

fn create_tensor_from_external(
    memory_info: &MemoryInfo,
    data: *mut c_void,
    elem_count: usize,
    shape: &[i64],
) -> OrtValueHandle {
    let mut value_ptr: *mut sys::OrtValue = std::ptr::null_mut();
    let status = unsafe {
        (api().CreateTensorWithDataAsOrtValue)(
            memory_info.ptr(),
            data,
            elem_count * std::mem::size_of::<f32>(),
            shape.as_ptr(),
            shape.len(),
            sys::ONNXTensorElementDataType::ONNX_TENSOR_ELEMENT_DATA_TYPE_FLOAT,
            &mut value_ptr,
        )
    };
    check_status(status, "CreateTensorWithDataAsOrtValue failed");
    OrtValueHandle(NonNull::new(value_ptr).expect("ORT returned null OrtValue"))
}

struct BatchCompletion {
    state: AtomicU8,
    error: Mutex<Option<String>>,
}

impl BatchCompletion {
    fn new() -> Self {
        Self {
            state: AtomicU8::new(BATCH_PENDING),
            error: Mutex::new(None),
        }
    }

    fn wait(&self) {
        loop {
            match self.state.load(Ordering::Acquire) {
                BATCH_PENDING => std::hint::spin_loop(),
                BATCH_READY => return,
                BATCH_FAILED => {
                    let message = self
                        .error
                        .lock()
                        .expect("poisoned BatchCompletion error lock")
                        .clone()
                        .unwrap_or_else(|| "unknown ORT async failure".to_string());
                    eprintln!("RunAsync completion failed: {message}");
                    std::process::exit(1);
                }
                _ => unreachable!("invalid batch completion state"),
            }
        }
    }
}

unsafe extern "system" fn run_async_callback(
    user_data: *mut c_void,
    _outputs: *mut *mut sys::OrtValue,
    _num_outputs: usize,
    status: sys::OrtStatusPtr,
) {
    let completion = unsafe { Arc::from_raw(user_data.cast::<BatchCompletion>()) };
    if status.0.is_null() {
        completion.state.store(BATCH_READY, Ordering::Release);
    } else {
        *completion
            .error
            .lock()
            .expect("poisoned BatchCompletion error lock") = Some(status_message(status));
        completion.state.store(BATCH_FAILED, Ordering::Release);
    }
}

fn alloc_pinned(bytes: usize) -> *mut c_void {
    unsafe {
        let mut ptr: *mut c_void = std::ptr::null_mut();
        let status = cudarc::driver::sys::cuMemAllocHost_v2(&mut ptr, bytes);
        if status != cudarc::driver::sys::CUresult::CUDA_SUCCESS {
            eprintln!("cuMemAllocHost_v2 failed: {:?}", status);
            std::process::exit(1);
        }
        ptr
    }
}

fn free_pinned(ptr: *mut c_void) {
    unsafe {
        let status = cudarc::driver::sys::cuMemFreeHost(ptr);
        if status != cudarc::driver::sys::CUresult::CUDA_SUCCESS {
            eprintln!("cuMemFreeHost failed: {:?}", status);
            std::process::exit(1);
        }
    }
}

fn generated_model_input_value(batch_idx: usize, feature_idx: usize) -> f32 {
    (batch_idx as f32 + 1.0) * 10.0 + feature_idx as f32 + 1.0
}

fn generated_model_expected(batch_idx: usize) -> f32 {
    (0..FDIM)
        .map(|feature_idx| {
            generated_model_input_value(batch_idx, feature_idx) * (feature_idx as f32 + 1.0)
        })
        .sum()
}

fn main() {
    let args = Args::parse();
    let model_bytes = std::fs::read(&args.model).unwrap_or_else(|e| {
        eprintln!("Failed to read model '{}': {e}", args.model);
        std::process::exit(1);
    });

    let session = Session::builder()
        .unwrap_or_else(|e| {
            eprintln!("Session::builder failed: {e}");
            std::process::exit(1)
        })
        .with_intra_threads(2)
        .unwrap_or_else(|e| {
            eprintln!("with_intra_threads failed: {e}");
            std::process::exit(1)
        })
        .with_execution_providers([CUDAExecutionProvider::default().build()])
        .unwrap_or_else(|e| {
            eprintln!("with_execution_providers failed: {e}");
            std::process::exit(1)
        })
        .commit_from_memory(&model_bytes)
        .unwrap_or_else(|e| {
            eprintln!("commit_from_memory failed: {e}");
            std::process::exit(1)
        });

    if session.inputs().len() != 1 || session.outputs().len() != 1 {
        eprintln!(
            "ort-verify expects exactly 1 input and 1 output, got {} inputs / {} outputs",
            session.inputs().len(),
            session.outputs().len()
        );
        std::process::exit(1);
    }

    let input_name = CString::new(session.inputs()[0].name()).unwrap_or_else(|e| {
        eprintln!("invalid input name: {e}");
        std::process::exit(1)
    });
    let output_name = CString::new(session.outputs()[0].name()).unwrap_or_else(|e| {
        eprintln!("invalid output name: {e}");
        std::process::exit(1)
    });

    let input_memory_info = MemoryInfo::new(
        AllocationDevice::CUDA_PINNED,
        0,
        AllocatorType::Device,
        MemoryType::CPUInput,
    )
    .unwrap_or_else(|e| {
        eprintln!("input MemoryInfo::new failed: {e}");
        std::process::exit(1)
    });
    let output_memory_info = MemoryInfo::new(
        AllocationDevice::CUDA_PINNED,
        0,
        AllocatorType::Device,
        MemoryType::CPUOutput,
    )
    .unwrap_or_else(|e| {
        eprintln!("output MemoryInfo::new failed: {e}");
        std::process::exit(1)
    });

    let input_elems = args.batch * FDIM;
    let input_ptr = alloc_pinned(input_elems * std::mem::size_of::<f32>()) as *mut f32;
    let output_ptr = alloc_pinned(args.batch * std::mem::size_of::<f32>()) as *mut f32;
    unsafe {
        for batch_idx in 0..args.batch {
            for feature_idx in 0..FDIM {
                let offset = batch_idx * FDIM + feature_idx;
                *input_ptr.add(offset) = if args.validate_generated_model {
                    generated_model_input_value(batch_idx, feature_idx)
                } else {
                    1.0
                };
            }
        }
        std::ptr::write_bytes(output_ptr, 0, args.batch * std::mem::size_of::<f32>());
    }

    let input_shape = [args.batch as i64, FDIM as i64];
    let output_shape = [args.batch as i64];
    let input_value = create_tensor_from_external(
        &input_memory_info,
        input_ptr.cast::<c_void>(),
        input_elems,
        &input_shape,
    );
    let output_value = create_tensor_from_external(
        &output_memory_info,
        output_ptr.cast::<c_void>(),
        args.batch,
        &output_shape,
    );

    let completion = Arc::new(BatchCompletion::new());
    let callback_completion = Arc::clone(&completion);
    let user_data = Arc::into_raw(callback_completion) as *mut c_void;

    let input_names = [input_name.as_ptr() as *const c_char];
    let output_names = [output_name.as_ptr() as *const c_char];
    let input_values = [input_value.as_ptr() as *const sys::OrtValue];
    let mut output_values = [output_value.as_ptr()];

    let status = unsafe {
        (api().RunAsync)(
            session.ptr().cast_mut(),
            std::ptr::null(),
            input_names.as_ptr(),
            input_values.as_ptr(),
            input_values.len(),
            output_names.as_ptr(),
            output_names.len(),
            output_values.as_mut_ptr(),
            Some(run_async_callback),
            user_data,
        )
    };
    if !status.0.is_null() {
        unsafe { drop(Arc::from_raw(user_data.cast::<BatchCompletion>())) };
        check_status(status, "RunAsync failed");
    }

    completion.wait();

    let output = unsafe { std::slice::from_raw_parts(output_ptr, args.batch) };
    println!(
        "ort-verify ok: batch={}, output_name={}, values={}",
        args.batch,
        session.outputs()[0].name(),
        output.len()
    );
    if !output.is_empty() {
        println!(
            "output sample: first={:.6}, last={:.6}",
            output[0],
            output[output.len() - 1]
        );
    }

    if args.validate_generated_model {
        for (batch_idx, &actual) in output.iter().enumerate() {
            let expected = generated_model_expected(batch_idx);
            let diff = (actual - expected).abs();
            if diff > 1e-4 {
                eprintln!(
                    "generated-model validation failed at row {}: expected {:.6}, got {:.6}",
                    batch_idx, expected, actual
                );
                std::process::exit(1);
            }
        }
        println!("generated-model validation ok");
    }

    drop(output_value);
    drop(input_value);
    free_pinned(output_ptr.cast::<c_void>());
    free_pinned(input_ptr.cast::<c_void>());
}
